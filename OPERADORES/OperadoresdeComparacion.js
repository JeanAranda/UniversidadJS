//Operadores Relacionales (de Comparacion)
let a = 5
let b = '5'
console.log(a)
console.log(b)
//Operadores de igualdad == 
//Solo compara valores y hace una conversion si es necesario
console.log('a es igual b -->', a == b)
console.log(`${a} == ${b} --> ${a == b}`)
//Operador de igualdad estricta o exacta
//Se compara el valor y el tipo de dato
console.log('a es igual b -->', a === b)
//String interpolation
console.log(`${a} === ${b} --> ${a === b}`)
//Operadores distintos
//Compara valor y convierte el tipo de dato si es necesario
console.log(`${a} != ${b} --> ${a != b}`)
//Operador distinto exacto
//Compara el valor y el tipo de dato
console.log(`${a} !== ${b} --> ${a !== b}`)
//Operador de menor que
console.log(`${a} < ${b} --> ${a < b}`)
//Operador de menor o igual que
console.log(`${a} <= ${b} --> ${a <= b}`)
//Operador mayor que
console.log(`${a} > ${b} --> ${a > b}`)
//Operador mayor o igual que
console.log(`${a} >= ${b} --> ${a >= b}`)