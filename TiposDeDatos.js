//Tipos de datos en js
let miEntero = 10
console.log(miEntero)
//Se puede modificar el valor de la variable en js por su naturaleza dinamica del lenguaje
miEntero = "Jean"
console.log(miEntero)
//Tipo flotante
let miFlotante = 10.5
console.log(miFlotante)
//Tipo String o cadena
let miCadena = "Este es el valor de una cadena"
console.log(miCadena)
//Tipo boleano o boolean
let miBolean = true
console.log(miBolean)
//Tipo null o ausencia de la referencia de un objeto
let miNull = null
console.log(miNull)
